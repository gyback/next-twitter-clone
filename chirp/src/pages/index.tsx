const PostView = ({ post, author }: PostWithUser) => {
  return (
    <div key={post.id} className="flex gap-3 border-b border-slate-400 p-4">
      <Image
        src={author.profilePicture}
        alt={`Profile picture of ${author.name}`}
        className="h-8 w-8 rounded-full align-bottom"
        width={56}
        height={56}
      />
      <section className="flex flex-col gap-3 text-slate-300">
        <div className="flex flex-row gap-1.5">
          <p>{`@${author.name}`}</p>

          <p className="font-thin">{`Â· ${dayjs(post.createdAt).fromNow()}`}</p>
        </div>
        <p>{post.content}</p>
      </section>
    </div>
  );
};

const User = () => {
  const { user } = useUser();
  if (user) return <UserButton />;
  return (
    <div className="flex w-full flex-col gap-3">
      <SignInButton />
    </div>
  );
};

const Feed = () => {
  const { data, isLoading: postsLoading } = api.posts.getAll.useQuery();

  if (postsLoading) return <LoadingPage />;
  if (!data) return <div>Something went wrong...</div>;
  const renderPosts = () => {};
  return (
    <section className="flex flex-col">{data.map((p) => PostView(p))}</section>
  );
};
